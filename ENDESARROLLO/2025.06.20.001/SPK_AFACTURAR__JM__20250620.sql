         DEALLOCATE PPTFTR_CURSOR
         SELECT @OK = COUNT(*) FROM HPRED, HPRE
         WHERE  HPRED.IDTERCEROCA  = @IDTERCEROCA
         AND    HPRED.IDPLAN       = @IDPLAN
         AND    HPRED.NOPRESTACION = HPRE.NOPRESTACION
         AND    HPRE.NOADMISION    = @NOADMISION

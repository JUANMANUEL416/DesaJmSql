            AND    ISNULL(HPRED.ANULADO,0) = 0
            AND    ISNULL(HPRE.ANULADO,0) = 0
            AND    HPRE.IDAREA        NOT IN (SELECT PPTFTRD.IDAREA FROM PPTFTRD
                                              WHERE PPTFTRD.IDTERCERO  = @IDTERCEROCA

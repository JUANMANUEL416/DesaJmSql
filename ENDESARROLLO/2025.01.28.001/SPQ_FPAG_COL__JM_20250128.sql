		INNER JOIN FIMPD ON FPAGDI.IDCLASE =  FIMPD.IDCLASE AND FIMPD.IDIMPUESTO = FPAGDI.IDIMPUESTO
		WHERE COALESCE(FPAGDI.N_FACTURA, '') = @N_FACTURA AND COALESCE(FPAGDI.CNSFPAG, '') = @CNSFPAG
		RETURN
	END


IF NOT EXISTS ( SELECT O.object_id, C.column_id, C.NAME FROM  SYS.OBJECTS O INNER JOIN SYS.columns C ON O.object_id = C.object_id  WHERE O.NAME = 'FPAG'  AND   C.NAME = 'ENAUDIT')
BEGIN
   ALTER TABLE FPAG  ADD ENAUDIT BIT
END
IF NOT EXISTS ( SELECT O.object_id, C.column_id, C.NAME FROM  SYS.OBJECTS O INNER JOIN SYS.columns C ON O.object_id = C.object_id  WHERE O.NAME = 'FPAG'  AND   C.NAME = 'CNSENTREGA')
BEGIN
   ALTER TABLE FPAG  ADD CNSENTREGA VARCHAR(20)
END
IF NOT EXISTS(SELECT NAME FROM SYSOBJECTS WHERE NAME='PPTMOVI' AND XTYPE='U')
BEGIN
   CREATE TABLE PPTMOVI (
   ID INT IDENTITY (1,1) NOT NULL,
   IDTERCERO VARCHAR(20) NOT NULL,
   IDPLAN VARCHAR(6) NOT NULL,
   CIUDAD VARCHAR(20)NOT NULL,
   DISTRITO VARCHAR(20)NOT NULL,
   VALOR DECIMAL(14,2),
   ZONA VARCHAR(20)
   )
END
IF NOT EXISTS(SELECT  * FROM SYS.objects WHERE name='PPTMOVIID')
BEGIN
   ALTER TABLE PPTMOVI  ADD CONSTRAINT PPTMOVIID PRIMARY KEY CLUSTERED (ID)
END
IF NOT EXISTS(SELECT * FROM SYS.indexes WHERE name='PPTMOVIIDTERCEROIDPLANCIUDADDISTRITO')
BEGIN
   CREATE INDEX PPTMOVIIDTERCEROIDPLANCIUDADDISTRITO ON PPTMOVI (IDTERCERO,IDPLAN,CIUDAD,DISTRITO)
END
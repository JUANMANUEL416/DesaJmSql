      BEGIN
         IF DBO.FNK_VALORVARIABLE('FACTSEDE')='SI'
         BEGIN
            SELECT  @CNSRESOL=CNSRESOL,@PREFIJOFAC=PREFIJO  FROM FDIAN WHERE IDENTIFICADOR=@IDSEDE AND VENCIDA=0 AND PROCEDENCIA=  CASE WHEN  @TIPOFAC='Boleta' THEN 'BOLE' ELSE 'FTR' END

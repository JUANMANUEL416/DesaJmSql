   SELECT @VR_TOTAL = SUM(VR_TOTAL) FROM FNOTD WHERE CNSFNOT = @CNSFNOT AND CLASE = @CLASE
   UPDATE FNOT SET VR_TOTAL = CASE WHEN DBO.FNK_VALORVARIABLE('IXCOUNTRY')='PERU' THEN ROUND(@VR_TOTAL,2) ELSE @VR_TOTAL END,DETGENERADOS = 1 WHERE CNSFNOT = @CNSFNOT AND CLASE = @CLASE
   IF EXISTS(SELECT * FROM FNOT WHERE CNSFNOT=@CNSFNOT AND PROCEDENCIA='FACTURA' AND COALESCE(IDCONCEPTO,'')='')
   BEGIN
      UPDATE FNOT SET IDCONCEPTO=DBO.FNK_VALORVARIABLE('IDCONCEP_ANULAFTRCE') WHERE CNSFNOT=@CNSFNOT AND PROCEDENCIA='FACTURA' AND COALESCE(IDCONCEPTO,'')=''
   END

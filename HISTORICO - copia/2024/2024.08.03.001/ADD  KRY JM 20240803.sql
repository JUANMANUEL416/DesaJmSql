IF NOT EXISTS ( SELECT O.object_id, C.column_id, C.NAME FROM  SYS.OBJECTS O INNER    JOIN SYS.columns C ON O.object_id = C.object_id  
               WHERE O.NAME = 'ACJ'  AND   C.NAME = 'CONTABILIZADA')
BEGIN
   ALTER TABLE  ACJ  ADD CONTABILIZADA BIT
END
IF NOT EXISTS ( SELECT O.object_id, C.column_id, C.NAME FROM  SYS.OBJECTS O INNER    JOIN SYS.columns C ON O.object_id = C.object_id  
               WHERE O.NAME = 'ACJ'  AND   C.NAME = 'NROCOMPROBANTE')
BEGIN
   ALTER TABLE  ACJ  ADD NROCOMPROBANTE VARCHAR(20)
END
